class Solution {
    public int[] plusOne(int[] digits) {
        int[] ans = new int[digits.length + 1];
        int k = 1;
        for (int i = digits.length,j = digits.length-1; j >= 0 ; i--,j--) {
            int digit = k + digits[j];
            if (digit == 10) {
                ans[i] = 0;
            } else {
                ans[i] = digit;
                k = 0;
            }
        }
        if (k == 1) {
            ans[0] = 1;
            return ans;
        } else {
            return Arrays.copyOfRange(ans,1,digits.length+1);
        }
    }
}
